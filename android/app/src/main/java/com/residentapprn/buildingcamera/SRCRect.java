public class SRCRect
{
    public int srcLeft;
    public int srcRight;
    public int srcTop;
    public int srcBottom;

    public void setSrcLeft(int left)
    {
        srcLeft = left;
    }

    public int getSrcLeft()
    {
        return srcLeft;
    }

    public void setSrcRight(int right)
    {
        srcRight = right;
    }

    public int getSrcRight()
    {
        return srcRight;
    }

    public void setSrcTop(int top)
    {
        srcTop = top;
    }

    public int getSrcTop()
    {
        return srcTop;
    }

    public void setSrcBottom(int bottom)
    {
        srcBottom = bottom;
    }

    public int getSrcBottom()
    {
        return srcBottom;
    }

}
